    public void loadTabel(){
        tabelModel.getDataVector().removeAllElements();
        tabelModel.fireTableDataChanged();
        
        try {
            Connection connection = Koneksi.getConnection();
            Statement statement = connection.createStatement();
            String sql = "SELECT * FROM pendaftar";
            ResultSet resultSet = statement.executeQuery(sql);
            
            while(resultSet.next()){
                Object[] objects = new Object[6];
                
                objects[0] = resultSet.getString("NIK");
                objects[1] = resultSet.getString("Nama");
                objects[2] = resultSet.getString("jkel");
                if(objects[2]=="l"){
                   objects[2]="laki laki"; 
                }
                else if(objects[2]=="p"){
                    objects[2]="perempuan"; 
                }
                else{
                    objects[2]="maho";
                }
                objects[3] = resultSet.getString("alamat");
                objects[4] = resultSet.getDate("tanggal_mendaftar");
                objects[5] = resultSet.getString("jenis_lomba");
                switch((String)objects[5]){
                    case "1":
                        objects[5] = "Makan Kerupuk";
                        break;
                    case "2":
                        objects[5] = "Menyanyi";
                        break;
                    case "3":
                        objects[5] = "Pentung Air";
                        break;
                }
                tabelModel.addRow(objects);
            }
            
            resultSet.close();
            statement.close();  
        } catch (SQLException e) {
            System.out.println("Error : "+e);
        }
    }

