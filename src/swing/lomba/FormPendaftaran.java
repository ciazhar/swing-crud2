    private void buttonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateActionPerformed
        String nik = inputNIK.getText();
        String nama = inputNama.getText();
        String jenisKelamin = inputJenisKelamin.getText();
        String alamat = inputAlamat.getText();
        java.util.Date utilDate = new java.util.Date();
        java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
        String jenisLomba = inputJenisLomba.getText();
        try {
            Connection connection = Koneksi.getConnection();
            String query = "insert into pendaftar values(?,?,?,?,?,?)";
            
            PreparedStatement statement = connection.prepareStatement(query);
            
            statement.setString(1, nik);
            statement.setString(2, nama);
            statement.setString(3, jenisKelamin);
            statement.setString(4, alamat);
            statement.setDate(5, sqlDate);
            statement.setString(6, jenisLomba);
            
            statement.executeUpdate();
            statement.close();
            System.out.println("sukses");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Create Failed"+e);
        }
        finally{
            loadTabel();
        }
    }//GEN-LAST:event_buttonCreateActionPerformed

    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        int i = tabelPendaftaran.getSelectedRow();
        if(i==-1){
            return;
        }
        
        String id = (String) tabelModel.getValueAt(i, 0);
        
        try {
            Connection c = Koneksi.getConnection();
            
            String sql = "DELETE FROM detil_barang where id_barang = ?";
            
            PreparedStatement ps = c.prepareStatement(sql);
            
            ps.setString(1, id);
            
            ps.executeUpdate();
            ps.close();
        } catch (SQLException e) {
            System.out.println("Error "+e);
        }
        finally{
            loadTabel();
        }
    }//GEN-LAST:event_buttonUpdateActionPerformed


